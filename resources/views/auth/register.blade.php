<script type="text/javascript">
$(document).ready(function() {
    $('#nif-row').hide();
    $('input:radio[name=role]').on('change', function(){
        var user_type = $('input:radio[name=role]:checked').val();
        if(user_type === 'trainer'){
            $('#nif-row').hide('slow');
            $('#nif').removeAtt('required');
            $('#surname-row').show('slow');
            $('#surname').attr('required', 'required');
        }
        if(user_type === 'institution'){
            $('#nif-row').show('slow');
            $('#nif').attr('required', 'required');
            $('#surname-row').hide('slow');
            $('#surname').removeAtt('required');
        }
    });
});
</script>
